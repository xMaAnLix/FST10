// Functions
// Reusable block of codes that can be executed when called

// Function Definition - creating a function
function greet() {
  console.log("Hello");
  console.log("Good Afternoon");
}

// invoking a function - calling a function
greet();
greet();

// Function definition with parameters
function sum(num1, num2) {
  return num1 + num2;
}

//  Calling a function
// arguments
console.log(sum(1, 1));
console.log(sum(5, 10));
